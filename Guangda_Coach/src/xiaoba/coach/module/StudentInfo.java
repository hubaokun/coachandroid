package xiaoba.coach.module;

public class StudentInfo {
	int studentid;
	String avatar;
	String phone;
	String student_cardnum;
	String realname;
	int coachstate;
	String avatarurl;

	public String getAvatarurl() {
		return avatarurl;
	}

	public void setAvatarurl(String avatarurl) {
		this.avatarurl = avatarurl;
	}

	public int getStudentid() {
		return studentid;
	}

	public void setStudentid(int studentid) {
		this.studentid = studentid;
	}

	public String getAvatar() {
		return avatar;
	}

	public void setAvatar(String avatar) {
		this.avatar = avatar;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getStudent_cardnum() {
		return student_cardnum;
	}

	public void setStudent_cardnum(String student_cardnum) {
		this.student_cardnum = student_cardnum;
	}

	public String getRealname() {
		return realname;
	}

	public void setRealname(String realname) {
		this.realname = realname;
	}

	public int getCoachstate() {
		return coachstate;
	}

	public void setCoachstate(int coachstate) {
		this.coachstate = coachstate;
	}

}
